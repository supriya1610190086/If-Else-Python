day=input("enter the day")
if day=="monday":
    print("kritika","saloni","gauri","mayuri")
elif day=="tuesday":
    print("siddhi","anamika","karuna","priyanka")
elif day=="wednesday":
    print("karishma","chhaya","sonam","raksha")
elif day=="thursday":
    print("namrata","kusum","nikita","mamta")
elif day=="friday":
    print("gunjan","usha","ardhyngya","tejashri")
elif day=="saturday":
    print("shewta","madhu","swati","ankita")
elif day=="sunday":
    print("rani","kimaya","supriya","preeti")
else:
    print("invalid day")